// ─── Helper — detects project type from project config ──────────────

async function detectProjectConfig(): Promise<ProjectConfig> {
  try {
    const result = await (window as any).openllmcode?.api?.fs?.readFile?.('package.json');
    if (result) return { type: 'node', testCommand: 'npm test', buildCommand: 'npm run build' };
    
    // Check for Go module
    const goMod = await (window as any).openllmcode?.api?.fs?.readFile?.('go.mod');
    if (goMod) return { type: 'go', testCommand: 'go test ./...', buildCommand: 'go build' };
    
    // Check for Rust project
    const cargoToml = await (window as any).openllmcode?.api?.fs?.readFile?.('Cargo.toml');
    if (cargoToml) return { type: 'rust', testCommand: 'cargo test', buildCommand: 'cargo build' };
    
    // Check for Python project
    const setupPy = await (window as any).openllmcode?.api?.fs?.readFile?.('setup.py');
    if (setupPy) return { type: 'python', testCommand: 'pytest', buildCommand: 'pip install -e .' };
    
    // Check for requirements.txt
    const reqs = await (window as any).openllmcode?.api?.fs?.readFile?.('requirements.txt');
    if (reqs) return { type: 'python', testCommand: 'pytest', buildCommand: 'pip install -r requirements.txt' };
  } catch { /* file read failed */ }
  
  return { type: 'unknown' };
}

// ─── Helper — detects Git origin URL and current branch ──────────────

async function getGitConfig(): Promise<GitConfig> {
  try {
    const origin = await (window as any).openllmcode?.api?.execCommand('git remote get-url origin');
    if (origin) return { originUrl: origin, currentBranch: null };
    
    // Fallback: read .git/config directly
    let gitConfigContent: string | null = null;
    try {
      gitConfigContent = await (window as any).openllmcode?.api?.fs?.readFile?.('.git/config') ?? null;
    } catch { /* read failed */ }
    
    if (!gitConfigContent) return { originUrl: null, currentBranch: null };
    
    const originMatch = gitConfigContent.match(/\[remote "origin"\][\s\S]*?url\s*=\s*(.+?)\n/);
    const branchMatch = gitConfigContent.match(/\[branch "[^"]+"\]/);
    
    return {
      originUrl: originMatch?.[1]?.trim() || null,
      currentBranch: branchMatch ? `"${branchMatch[1]}"` : null,
    };
  } catch {
    return { originUrl: null, currentBranch: null };
  }
}

// ─── Helper — parses GitHub/GitLab URL into owner/repo ──────────────

function parseRepoUrl(url: string): { platform: 'github' | 'gitlab'; owner: string; repo: string } | null {
  // GitHub: https://github.com/owner/repo
  const ghMatch = url.match(/https:\/\/github\.com\/([^/]+)\/([^/]+)/);
  if (ghMatch) return { platform: 'github', owner: ghMatch[1], repo: ghMatch[2] };
  
  // GitLab: https://gitlab.com/owner/repo
  const glMatch = url.match(/https:\/\/gitlab\.com\/([^/]+)\/([^/]+)/);
  if (glMatch) return { platform: 'gitlab', owner: glMatch[1], repo: glMatch[2] };
  
  // GitHub Enterprise: https://<host>/owner/repo
  const ghEnt = url.match(/https:\/\/(.+)\/([^/]+)\/([^/]+)/);
  if (ghEnt && ghEnt[3].match(/^[\w.-]+$/) && !ghEnt[2]!.includes('.')) {
    return { platform: 'github', owner: ghEnt[2], repo: ghEnt[3] };
  }
  
  return null;
}

// ─── Helper — generates a GitHub Actions workflow YAML with project-aware configuration ──────────────

function generateWorkflowYaml(architectures: ArchitectureType[], name: string, config?: ProjectConfig): string {
  const archList = architectures.map(a => `"${a}"`).join(', ');
  
  // GitHub Actions uses ${{ }} which conflicts with JS template literals, so we use a placeholder pattern
  const ghAction = (expr: string) => `\${{ ${expr} }}`;

  let yamlLines = [
    '# Generated by OpenLLMCode CI/CD Panel — architecture-aware multi-architecture test matrix',
    `# Project type: ${config?.type || 'unknown'} | Test command: ${config?.testCommand || 'not detected'}`,
    '',
    `name: "${(name && name.trim()) || ((config && config.type !== 'unknown') ? config.type + ' Pipeline' : 'OpenLLMCode CI Pipeline')}"`,
    '',
    'on:',
    '  push:',
    '    branches: [main, master]',
    '  pull_request:',
    '    branches: [main, master]',
    '',
    'jobs:',
    '  build-and-test:',
    '    runs-on: ubuntu-latest',
    '    strategy:',
    '      matrix:',
    `        architecture: [${archList}]`,
    '    ',
    '    steps:',
    '      - uses: actions/checkout@v4',
  ];

  // Node.js — install dependencies and run test command
  if (config?.type === 'node') {
    yamlLines.push(
      '',
      `      - name: Setup Node.js`,
      '        uses: actions/setup-node@v4',
      '        with:',
      '          node-version: "20"',
      '          cache: "npm"',
      '',
      '      - name: Install dependencies',
      '        run: npm ci',
      '',
      `      - name: Build`,
      `        run: ${config.buildCommand || 'npm run build'}`,
    );
  }

  // Go — install go and run test command
  if (config?.type === 'go') {
    yamlLines.push(
      '',
      `      - name: Setup Go`,
      '        uses: actions/setup-go@v5',
      '        with:',
      '          go-version: "1.22"',
      '',
      '      - name: Install dependencies',
      '        run: go mod download',
      '',
      `      - name: Test`,
      `        run: ${config.testCommand || 'go test ./...'}`,
    );
  }

  // Rust — install rustup and run cargo test/build
  if (config?.type === 'rust') {
    yamlLines.push(
      '',
      `      - name: Setup Rust`,
      '        uses: dtolnay/rust-toolchain@stable',
      '',
      '      - name: Install dependencies',
      '        run: cargo fetch',
      '',
      `      - name: Build`,
      `        run: ${config.buildCommand || 'cargo build'}`,
    );
  }

  // Python — setup python and pytest
  if (config?.type === 'python') {
    yamlLines.push(
      '',
      `      - name: Setup Python`,
      '        uses: actions/setup-python@v5',
      '        with:',
      '          python-version: "3.12"',
      '',
      '      - name: Install dependencies',
      '        run: |',
      '          pip install pytest',
      config?.buildCommand ? `          ${config.buildCommand}` : '          pip install -r requirements.txt',
    );
  }

  // KVM acceleration step for x86_64
  if (architectures.some(a => a === 'x86_64')) {
    yamlLines.push(
      '',
      `      - name: Enable KVM acceleration`,
      '        run: |',
      '          echo "KVM available on host — using native x86_64 execution"',
      '          sudo apt-get install -y qemu-kvm',
    );
  }

  // Cross-architecture compile step for non-x86_64 architectures
  if (!architectures.some(a => a === 'x86_64')) {
    yamlLines.push(
      '',
      '# Cross-architecture compile (if not x86_64) — per QEMU multi-arch compilation docs',
      `      - name: Set up QEMU for ${ghAction('matrix.architecture')}`,
      '        uses: docker/setup-qemu-action@v3',
    );
  }

  // Architecture-specific artifact upload
  yamlLines.push(
    '',
    '# Architecture-specific artifact upload — per QEMU docs for cross-arch compilation parallelism',
    '      - name: Upload artifacts',
    '        uses: actions/upload-artifact@v4',
    '        with:',
    `          name: build-${ghAction('matrix.architecture')}`,
    '          path: dist/',
  );

  return yamlLines.join('\n');
}

// ─── Helper — creates a GitHub PR via API using PAT authentication ──────────────

async function createGitHubPR(repoUrl: string, branchName: string): Promise<GitHubPRResponse | null> {
  // Auto-detect repo URL from .git/config if not provided
  let resolvedRepoUrl = repoUrl;
  if (!repoUrl) {
    const gitConfig = await getGitConfig();
    if (gitConfig.originUrl) {
      resolvedRepoUrl = gitConfig.originUrl.replace(/\.git$/, ''); // Remove trailing .git for API URL
    } else {
      return null; // No origin configured
    }
  }

  const parsed = parseRepoUrl(resolvedRepoUrl);
  if (!parsed || parsed.platform !== 'github') return null;

  // Get PAT from Electron store config — uses the same auth system as hfClient.ts for HuggingFace
  const config: Record<string, unknown> | null = await (window as any).openllmcode?.api?.electronStore?.getConfig();
  const pat = typeof config?.github_pat === 'string' ? config.github_pat : '';
  
  if (!pat) {
    throw new Error('GitHub PAT not configured. Set it in Settings → Auth tab.');
  }

  // Use the current branch from git config as the source branch for the PR
  const gitConfig = await getGitConfig();
  const headBranch = gitConfig.currentBranch?.replace(/"/g, '') || 'main';

  // Create a Pull Request via GitHub API — per https://docs.github.com/en/rest/pulls/pull-requests#create-a-pull-request
  const response = await fetch(`https://api.github.com/repos/${parsed.owner}/${parsed.repo}/pulls`, {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${pat}`,
      'Accept': 'application/vnd.github.v3+json',
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      title: `OpenLLMCode review: "${branchName}"`,
      head: headBranch,
      base: parsed.platform === 'github' ? (gitConfig.currentBranch?.replace(/"/g, '') || 'main') : 'main',
      body: `## OpenLLMCode Code Review\n\nAutomatically triggered by OpenLLMCode CI/CD Panel.\n\n- **Source branch**: ${headBranch}\n- **Target branch**: ${(gitConfig.currentBranch?.replace(/"/g, '')) || 'main'}\n`,
    }),
  });

  if (!response.ok) {
    const error = await response.json().catch(() => null);
    throw new Error(error?.message || `GitHub API error: ${response.status}`);
  }

  return (await response.json()) as GitHubPRResponse;
}

// ─── Helper — creates a GitLab MR via API using Personal Access Token authentication ──────────────

async function createGitlabMR(repoUrl: string, branchName: string): Promise<{ url: string; iid: number } | null> {
  const parsed = parseRepoUrl(repoUrl);
  if (!parsed || parsed.platform !== 'gitlab') return null;

  // Get GitLab PAT from Electron store config — same pattern as GitHub PAT above
  const config: Record<string, unknown> | null = await (window as any).openllmcode?.api?.electronStore?.getConfig();
  const pat = typeof config?.gitlab_pat === 'string' ? config.gitlab_pat : '';
  
  if (!pat) {
    throw new Error('GitLab PAT not configured. Set it in Settings → Auth tab.');
  }

  // Use the current branch from git config as the source branch for the MR
  const gitConfig = await getGitConfig();
  const headBranch = gitConfig.currentBranch?.replace(/"/g, '') || 'main';

  // Create a Merge Request via GitLab API — per https://docs.gitlab.com/ee/api/merge_requests.html#create-mr
  const response = await fetch(`https://gitlab.com/api/v4/projects/${encodeURIComponent(parsed.owner + '/' + parsed.repo)}/merge_requests`, {
    method: 'POST',
    headers: {
      'PRIVATE-TOKEN': pat,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      title: `OpenLLMCode review: "${branchName}"`,
      source_branch: headBranch,
      target_branch: (gitConfig.currentBranch?.replace(/"/g, '')) || 'main',
      description: `## OpenLLMCode Code Review\n\nAutomatically triggered by OpenLLMCode CI/CD Panel.\n\n- **Source branch**: ${headBranch}\n`,
    }),
  });

  if (!response.ok) {
    const error = await response.json().catch(() => null);
    throw new Error(error?.message || `GitLab API error: ${response.status}`);
  }

  return (await response.json()) as unknown as { url: string; iid: number };
}

